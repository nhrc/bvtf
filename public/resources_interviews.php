    <h3>Rev. Dr. Mike Van Buren - Fellowship Reformed of Hudsonville, MI</h3>
    <p>Presented his view on:</p>
    <ul>
      <li>The church's foundation must be the Word of God.</li>
      <li>When it comes to church structure, ask questions that no one has asked before.  If questions were once dismissed, ask them again.</li>
      <li>Define your direction (Staff comes up with their dreams, consistory makes the decisions, teams give direction).</li>
      <li>The church grows in proportion to the organizational structure that is put into place.</li>
      <li>Attendance drove the building projects.</li>
    </ul>

    <h3>Rev. Jonathan Elgersma - Faith Reformed Church of Zeeland, MI</h3>
    <p>Presented his view on:</p>
    <ul>
      <li>When you set your budget, account for costs of furnishings, heating/cooling/maintaining building, potential additional staff members, etc.</li>
      <li>Build programs around the people of the church, NOT the pastor.  Church needs to be able to maintain programs even if pastor leaves.</li>
    </ul>

