    <h1>RECOMMENDATIONS</h1>
    <h2>NARTHEX</h2>

    <p>(fellowship area behind the sanctuary)</p>

    <h3>Discoveries from Questionnaires</h3>
    <ul>
      <li>Improve wheelchair and handicap accessibility.</li>
      <li>Increase seating area.</li>
      <li>Design layout to improve pedestrian flow throughout the church.</li>
      <li>Increase communication of areas of the church by appropriate placement of directional signs.</li>
      <li>This area needs to be welcoming to visitors and comfortable.</li>
      <li>This area should be the center point of the church that will lead to other main areas of the church.</li>
    </ul>

    <h3>Recommendations</h3>
    <p>In the questionnaires, people viewed the narthex as an area that needs attention and is a top priority.  The BVTF recommends that changes are made to the narthex in order to improve the overall pedestrian flow in and out of the sanctuary as well as reduce congestion during social functions. The narthex could also be expanded to direct traffic flow into other areas of the church such as a fellowship area, kitchen, social gathering area/rec room, classrooms and the nursery.</p>

