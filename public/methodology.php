    <h1>METHODOLOGY / RATIONALE</h1>

    <h4>WHY</h4>
    <p>There have been recent inquiries by various church members regarding future facility needs. In addition, there are others who are aware that there are funds designated for “a new building” and wonder if there is a plan for these funds. There also is a desire to make an intelligent decision regarding our parking lot improvements and how they coincide with the timing of any potential future construction.</p>

    <h4>WHAT</h4>
    <p>The Consistory has requested that a group of individuals be asked to investigate the facility needs of NHRC and to present a report that makes recommendations and identifies priorities relating to building changes/improvements/projects. The report will also include the reasoning for each recommendation and its priority. It will not be necessary to identify any costs associated with the recommendations.</p>

    <h4>WHO</h4>
    <p>Select up to 8 individuals who are visionary and who work well together to identify the facility needs at NHRC. These individuals will be male and female, young and old, new to the church and veterans at NHRC.</p>

    <h4>HOW</h4>
    <p>Keeping in mind the vision of NHRC, the Building Vision Task Force will be asked to:</p>
    <ol>
      <li>Meet with the leaders of NHRC ministries to find out what their facility hopes and needs are.</li>
      <li>Meet with community leaders, such as the NH school principal and township officials, to consider the needs of the community.</li>
      <li>Meet with other selected church members as needed.</li>
      <li>Gather thoughts and ideas from the members of the task force.</li>
    </ol>

    <h4>WHEN</h4>
    <p>Begin the process in May after individuals have been invited to join the task force and after a meeting has taken place explaining the Consistory’s desire for the Building Vision Task Force. This introductory meeting would be led by 2 or 3 selected Consistory members. The due date of the report to Consistory would be August 31, 2012.</p>

