    <h1>SUMMARY</h1>

    <p>The BVTF found this project to be challenging and of much larger scope than it appeared on the
surface. This teamâ€™s mission was to investigate the facility needs of NHRC and to present a report
that makes recommendations and identifies priorities relating to building changes, improvements, and
projects.</p>

    <p>We learned that it was rare to identify a need without that need having some sort of impact on another
functional area of our church.</p>

    <p>The BVTF found that the vast majority of the greater church congregation, ministry teams and even
those outside of North Holland were willing to assist by providing feedback to the questionnaires,
interviews and off-site visits.</p>

    <p>Recommendations from the BVTF suggest that actions be taken in a number of areas of our church
facilities. The Narthex, Fellowship Hall &amp; Kitchen, Exterior, Classrooms &amp; Meeting rooms, Offices and
Storage have been identified as areas that require attention.</p>

    <p>It should be noted that making recommendations on an order in which each should be handled is
difficult as in certain situations a remedy in one area may lead to an improvement in another. An 
example of that could be if a new Fellowship Hall and Kitchen was constructed a remedy for storage
may be found from the space where the current ones reside. Because of this it is our suggestion that
an architect or consultant be brought in to assist with the final layout of the facility.</p>

