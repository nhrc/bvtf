    <h1>RECOMMENDATIONS</h1>
    <h2>EXTERIOR</h2>

    <h3>Discoveries from Questionnaires</h3>
    <ul>
      <li>Repaving to help with cosmetics and safety.</li>
      <li>Move the Basketball hoop to an area that is flatter and not in the middle of parking lot.</li>
      <li>Potential signage closer to 120th. (check zoning ordinance)</li>
      <li>Landscaping near road that would not affect vision.</li>
      <li>Create a larger lawn area for out door gatherings.</li>
      <li>Signage for main building entrances.</li>
      <li>Potential need to create a more obvious entrance into the church on the east side pending decisions on the parking lot layout.</p>
    </ul>

    <h3>Recommendations</h3>
    <p>Based on information from the questionnaires, the congregation likes the exterior of our church.  Most felt that the stone work and stain glass windows were pleasing to the eye, and there were no immediate concerns.  There was, however, significant concern mentioned to areas that complement the church.  Concerns about the parking lot were expressed.  (The need for parking lot improvements had been identified prior to this report.)  Handicap parking, wheelchair accessibility and confusion about which doors to enter the church were other concerns mentioned in the questionnaires.  The current “main” entrance is hidden in back, which may not be noticed by visitors.  After entering the building, people often have a difficult time locating the sanctuary, classrooms, nursery, bathrooms, etc.</p>

