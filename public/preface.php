    <p>Introduction/Purpose of the Building Vision Task Force (BVTF)</p>
    <ul>
      <li>In the spring of 2012, the Consistory put a team of 10 church members together to make up a team called the Building Vision Task Force. This team’s mission was to investigate the facility needs of North Holland Reformed Church (NHRC) and to present a report that makes recommendations and identifies priorities relating to building changes/improvements/projects. The results are summarized in this presentation.</li>
      <li>To accomplish this task the BVTF conducted interviews, visited off-site facilities and distributed questionnaires to the NHRC congregation, staff, and ministry teams.</li>
