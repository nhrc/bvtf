    <h1>LESSONS LEARNED</h1>
    <p>Throughout this process the BVTF came across valuable topics, quotes or simply pieces of information that we felt important to document. In some cases the information may be nothing more than simply topics to be aware of for if and when actions were to be taken on future planning for NHRC's facilities.</p>
    <p>This <em>Lessons Learned</em> page is meant to be used as a resource to help encourage additional thinking in areas that otherwise may not be considered or thought of.</p>
    <ul>
      <li>Don't build around the pastor (pastors eventually leave); make sure the congregation is strong and will stay.</li>
      <li>Time release Law: Schools will drop kids off at the church for education programs
        <ul><li>Georgetown CRC has preschool and day care</li></ul>
      </li>
      <li>"Ministry Drove the Building needs": Pastor Mike – Fellowship Reformed</li>
      <li>God's Will for our church – Elders need to identify</li>
      <li>Do we have the staff to grow into that vision?</li>
      <li>Does our congregation want to grow and will they support what is needed to reach potential?</li>
      <li>Attendance accountability of the congregation – does the sign-in sheet accomplish what is intended or is something else needed?</li>
      <li>Consider phase roll out of new additions or changes to the building.  Understand the impact one thing may have on another.</li>
      <li>Keep in mind the additional staff that may be needed to maintain the new building.</li>
      <li>New building security measures, patrol during service.</li>
      <li>Have staff or proper people in place to make sure programs are in place so that the building is being used. Example: Youth leader to keep youth programs growing, Retired Pastor to grow programs for the seniors.</li>
      <li>The facility and programs need to be reviewed on a regular basis so that our programs and facility keep up with the needs of the congregation and the community.</li>
      <li>Donated items or areas of special sentimental value should be considered in any additions or changes.</li>
      <li>Consider working with architects to identify the phases to be worked on so they complement each other.</li>
      <li>While trying to achieve the goals mentioned in this report we must work to make sure we stay true to our church’s vision.</li>
    </ul>

