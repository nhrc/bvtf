    <h2>COMMUNITY</h2>

    <h3>Linda Jacobs - Executive Director of Good Samaritan Ministries in Holland, MI</h3>
    <p>The purpose of the interview was to ascertain community needs, specifically in North Holland area that NHRC could potentially meet in regard to its future building changes and/or additions.</p>
    <p>Linda reported that her ministry has seen an increasing need for services for migrant workers and their children. Specifically, Linda noted there is a need for services for migrant children such as day care and after school activities to keep children occupied while their parents work.</p>

    <h3>North Holland Elementary School</h3>
    <p>Volunteers are needed...</p>
    <ul>
      <li>as babysitters while the parents attend a parenting class that is offered at the school.</li>
      <li>for special occasions such as the fall open house, holiday classroom parties or their spring carnival.</li>
      <li>to run small group activities in the library and on the playground at recess.</li>
      <li>to monitor and help with lunch time in the lunch room and hallways.</li>
      <li>to help students with homework before school.</li>
    </ul>
    <p>The school building across the street is adequate for most of the students needs.  However, the parenting class at the school is in need of more volunteers to work with students on-site. In the future, it may be possible that the parenting class could be held at NHRC.  Volunteers from our church could provide child care/activities during the meeting.</p>

    <h3>Government Municipality: Ottawa County</h3>
    <p>NHRC is located in Holland Charter Township (HCT) with Olive Township (OT) directly north of New Holland Street.</p>
    <ul>
      <li>HCT has a number of developments in existence and approved future planned developments for approximately 500 plus homes.</li>
      <li>HCT zoning allows for additional development.</li>
      <li>OT has no large developments in the general area of NHRC. The area around Borculo and the north end of OT is expected to see the largest growth in residential single family dwellings.</li>
      <li>Economic conditions may have an impact on growth and should be regularly reviewed as part of long-term planning.</li>
    </ul>

