    <h3>Faith Reformed Church</h3>
    <p>We chose Faith Reformed Church to learn about challenges and ideas to consider during a building process.</p>
    <p>In the early 2000's Faith Reformed Church undertook a major building expansion, adding a multipurpose room, youth rooms, kitchen, and multiple classrooms.</p>
    <p>Following are some of the main ideas:</p>
    <ul>
      <li>Key card entry for security - logs people coming/going from the building, easy to set for various volunteers.</li>
      <li>Clear and understandable signs.</li>
      <li>Coffee area with large open space for fellowship.</li>
      <li>New elevators with easy accessibility.</li>
      <li>Wheelchair-accessible bathrooms on same level as entrance.</li>
      <li>Multiple lighting options in gym (gym lights vs. lights for worship services).</li>
      <li>Large youth room - requires sound system.</li>
      <li>Thumb print scanner to print name tags.</li>
      <li>Numerous seating areas.</li>
      <li>Larger mailboxes for members (large enough to fit small boxes and packages) - some near the floor and hard to access.</li>
    </ul>

